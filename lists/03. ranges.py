fruits = ['Platano','Kiwi', 'Manzana', 'pi√±a', 'melon', 'Mangos', 'Uvas']

# Con rangos podemos obtener subconjuntos de la lista. El numero del ultimo queda fuera del listado
print(fruits[2:5])

# El siguiente ejemplo selecciona hasta el indice 3 (incluido)
print(fruits[:4])

#desde el indice 3 hasta el final
print(fruits[3:])

# Esto imprime toda la lista
print(fruits[:])

#siempre es desde el rango inferior incluido y excluido el rango superior
print(fruits[-4:-1])